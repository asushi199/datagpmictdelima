import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import xlsx from "xlsx";

const workbookPath =
  "PENGEMASKINIAN GPB_GPICT, GP DELIMA & GPM DAERAH MANJUNG 2025 (Responses).xlsx";
const outDir = path.join("src", "data");
const seedPath = path.join("supabase", "seed.sql");
const jsonPath = path.join(outDir, "initial-data.json");

const roleColumns = [
  ["GPICT", 12, 13],
  ["DELIMA", 14, 15],
  ["GPM", 16, 17],
];

function normalizeSchoolCode(value) {
  return String(value ?? "")
    .toUpperCase()
    .replace(/[^A-Z0-9]/g, "");
}

function cleanText(value) {
  const text = String(value ?? "").trim();
  if (!text || text === "-") return "";
  return text.replace(/\s+/g, " ");
}

function cleanZone(value) {
  return cleanText(value).replace(/\s*\(.*/, "");
}

function asIso(value) {
  if (value instanceof Date) return value.toISOString();
  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? new Date().toISOString() : parsed.toISOString();
}

function sql(value) {
  if (value === null || value === undefined || value === "") return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

const workbook = xlsx.readFile(workbookPath, { cellDates: true });
const sheet = workbook.Sheets[workbook.SheetNames[0]];
const rows = xlsx.utils.sheet_to_json(sheet, { header: 1, defval: "" });

const submissions = rows
  .slice(1)
  .map((row) => {
    const schoolCode = normalizeSchoolCode(row[5]);
    if (!schoolCode) return null;
    const roles = roleColumns.map(([role, nameIndex, phoneIndex]) => ({
      role,
      teacherName: cleanText(row[nameIndex]),
      phone: cleanText(row[phoneIndex]),
    }));

    return {
      id: crypto.randomUUID(),
      submittedAt: asIso(row[0]),
      schoolCode,
      schoolName: cleanText(row[4]),
      zone: cleanZone(row[3]),
      submitterName: "",
      submitterPhone: "",
      source: workbookPath,
      roles,
    };
  })
  .filter(Boolean)
  .sort((a, b) => a.submittedAt.localeCompare(b.submittedAt));

fs.mkdirSync(outDir, { recursive: true });
fs.mkdirSync(path.dirname(seedPath), { recursive: true });
fs.writeFileSync(jsonPath, `${JSON.stringify(submissions, null, 2)}\n`);

const currentBySchool = new Map();
for (const submission of submissions) {
  currentBySchool.set(submission.schoolCode, submission);
}

const lines = [
  "-- Generated by npm run import:initial",
  "-- Run supabase/schema.sql first, then this seed file.",
  "begin;",
];

for (const submission of submissions) {
  lines.push(
    `insert into schools (code, name, zone) values (${sql(submission.schoolCode)}, ${sql(
      submission.schoolName,
    )}, ${sql(submission.zone)}) on conflict (code) do update set name = excluded.name, zone = excluded.zone;`,
  );
  lines.push(
    `insert into contact_versions (id, school_code, school_name, zone, submitted_at, submitter_name, submitter_phone, source) values (${sql(
      submission.id,
    )}, ${sql(
      submission.schoolCode,
    )}, ${sql(submission.schoolName)}, ${sql(submission.zone)}, ${sql(
      submission.submittedAt,
    )}, null, null, ${sql(submission.source)}) on conflict (id) do nothing;`,
  );
  for (const role of submission.roles) {
    lines.push(
      `insert into contact_roles (version_id, role, teacher_name, phone) values (${sql(
        submission.id,
      )}, ${sql(role.role)}, ${sql(role.teacherName)}, ${sql(
        role.phone,
      )}) on conflict (version_id, role) do update set teacher_name = excluded.teacher_name, phone = excluded.phone;`,
    );
  }
}

for (const submission of currentBySchool.values()) {
  lines.push(
    `update schools set current_version_id = ${sql(submission.id)}, name = ${sql(
      submission.schoolName,
    )}, zone = ${sql(submission.zone)} where code = ${sql(submission.schoolCode)};`,
  );
}

lines.push("commit;");
fs.writeFileSync(seedPath, `${lines.join("\n")}\n`);

console.log(
  `Imported ${submissions.length} submissions for ${currentBySchool.size} schools into ${jsonPath}.`,
);
